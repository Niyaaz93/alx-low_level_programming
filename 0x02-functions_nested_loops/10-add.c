/**
 * add - function that adds to integers and returns the result
 * Description: Function accepts 2 integers and then returns the result of their
 * addition
 * @a: first integer to be added
 * @b: second integer to be added
 * Return: result of integer addition
 */

int add(int a, int b)
{
	int result;

	result = a + b;

	return (result);
}
